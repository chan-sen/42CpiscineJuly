int	ft_iterative_factorial(int nb)
{
	int		j;
	int		k;

	j = 1;
	k = 1;
	if (nb < 0 || nb > 12)
		return (0);
	if (nb == 0 || nb == 1)
		return (1);
	while (j <= nb)
	{
		k *= j;
		j++;
	}
	return (k);
}
